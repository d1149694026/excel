  IS '科室代码';
COMMENT ON COLUMN PURCHASEBUDGET.DEPTNAME
  IS '科室名称';
COMMENT ON COLUMN PURCHASEBUDGET.SUBJECTCODE
  IS '科目代码';
COMMENT ON COLUMN PURCHASEBUDGET.SUBJECTNAME
  IS '科目名称';
COMMENT ON COLUMN PURCHASEBUDGET.ITEMSCODE
  IS '品目代码';
COMMENT ON COLUMN PURCHASEBUDGET.ITEMSNAME
  IS '品目名称';
COMMENT ON COLUMN PURCHASEBUDGET.METHODCODE
  IS '采购方式代码';
COMMENT ON COLUMN PURCHASEBUDGET.METHODNAME
  IS '采购方式名称';
COMMENT ON COLUMN PURCHASEBUDGET.DETAILDESC
  IS '详细要求';
COMMENT ON COLUMN PURCHASEBUDGET.PRICE
  IS '单价(元)';
