CREATE TABLE PURCHASEBUDGET(
ID NUMBER PRIMARY KEY,
YEAR NUMBER,
UNITCODE VARCHAR2(10),
UNITNAME VARCHAR2(100),
DEPTCODE VARCHAR2(20),
DEPTNAME VARCHAR2(100),
SUBJECTCODE VARCHAR2(20),
SUBJECTNAME VARCHAR2(100),
ITEMSCODE VARCHAR2(20),
ITEMSNAME VARCHAR2(100),
METHODCODE VARCHAR2(20),
METHODNAME VARCHAR2(100),
DETAILDESC VARCHAR2(1000),
PRICE NUMBER,
QUANTITY NUMBER,
AMOUNT NUMBER,
BUDGETUSE VARCHAR2(1000),
APPLYEDSUM NUMBER,
REMAIN NUMBER,
INPUTUSER VARCHAR2(20) default 'SYSTEM',
INPUTTIME DATE default SYSDATE,
UPDATEUSER VARCHAR2(20) default 'SYSTEM',
UPDATETIME DATE default SYSDATE,
REMARK VARCHAR2(1000)
)
-- ADD COMMENTS TO THE TABLE 
COMMENT ON TABLE PURCHASEBUDGET
  IS '采购预算表';
-- ADD COMMENTS TO THE COLUMNS 
